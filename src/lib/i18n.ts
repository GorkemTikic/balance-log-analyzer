/* -------- Friendly labels by language -------- */
export function friendlyLabel(label: string, lang: LocalLang): string {
  const T = TEXTS[lang] as any;
  if (T[label]) return T[label];

  const L = label.toUpperCase();
  if (L.includes("EVENT_CONTRACTS_ORDER")) return T.EVENT_CONTRACTS_ORDER;
  if (L.includes("EVENT_CONTRACTS_PAYOUT")) return T.EVENT_CONTRACTS_PAYOUT;
  if (L.startsWith("COIN_SWAP")) return T.COIN_SWAP_MIX;
  if (L.startsWith("AUTO_EXCHANGE")) return T.AUTO_EXCHANGE_MIX;
  if (L.includes("REALIZED")) return T.REALIZED_PNL;
  if (L.includes("COMMISSION") || L.includes("TRADING_FEE")) return T.COMMISSION;
  if (L.includes("FUNDING_FEE")) return T.FUNDING_FEE;
  if (L.includes("INSURANCE")) return T.INSURANCE_CLEAR;
  if (L.includes("REFERRAL")) return T.REFERRAL_KICKBACK;
  if (L.includes("GIFT")) return T.CASH_COUPON;
  if (L.startsWith("TRANSFER") || L.includes("STRATEGY_")) return T.TRANSFER;

  return label;
}

